package nexon.cyphers.app.model.ToosinModel;

public class Toosin {
    int rank;
    String playerId;
    String nickname;
    int ratingPoint;
    int winCount;
    int loseCount;
    int winningStreak;

    public int getRank() {
        return rank;
    }

    public String getPlayerId() {
        return playerId;
    }

    public String getNickname() {
        return nickname;
    }

    public int getRatingPoint() {
        return ratingPoint;
    }

    public int getWinCount() {
        return winCount;
    }

    public int getLoseCount() {
        return loseCount;
    }

    public int getWinningStreak() {
        return winningStreak;
    }
}
