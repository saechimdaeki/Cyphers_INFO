package nexon.cyphers.app.model.matching_Detail;

public class Item {
    private String itemId;
    private String itemName;
    private String slotCode;
    private String slotName;
    private String rarityCode;
    private String rarityName;
    private String equipSlotCode;
    private String equipSlotName;

    public String getItemId() {
        return itemId;
    }

    public void setItemId(String itemId) {
        this.itemId = itemId;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getSlotCode() {
        return slotCode;
    }

    public void setSlotCode(String slotCode) {
        this.slotCode = slotCode;
    }

    public String getSlotName() {
        return slotName;
    }

    public void setSlotName(String slotName) {
        this.slotName = slotName;
    }

    public String getRarityCode() {
        return rarityCode;
    }

    public void setRarityCode(String rarityCode) {
        this.rarityCode = rarityCode;
    }

    public String getRarityName() {
        return rarityName;
    }

    public void setRarityName(String rarityName) {
        this.rarityName = rarityName;
    }

    public String getEquipSlotCode() {
        return equipSlotCode;
    }

    public void setEquipSlotCode(String equipSlotCode) {
        this.equipSlotCode = equipSlotCode;
    }

    public String getEquipSlotName() {
        return equipSlotName;
    }

    public void setEquipSlotName(String equipSlotName) {
        this.equipSlotName = equipSlotName;
    }
}
